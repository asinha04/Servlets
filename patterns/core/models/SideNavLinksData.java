package org.kp.patterns.core.models;

import com.google.gson.annotations.Expose;

/**
 * This Java class maps to each of properties in the Links array of Side Nav
 * JSON Data feed.
 * 
 * @author Mohan Joshi
 *
 */

public class SideNavLinksData {

	@Expose
	private String label;

	@Expose
	private String url;

	@Expose
	private String id;

	@Expose
	private String amount;

	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * @param label
	 *            the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url
	 *            the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}

	/**
	 * @param amount
	 *            the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}

}
